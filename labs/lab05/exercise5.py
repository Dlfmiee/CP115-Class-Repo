# INPUT
Score1 = float(input("Enter First Score: "))
Score2 = float(input("Enter Second Score: "))
Score3 = float(input("Enter Third Score: "))

# PROCESS
TotalScore = Score1 + Score2 + Score3
AverageScore = TotalScore / 3

# OUTPUT
print("First Score: " + str(Score1))
print("Second Score: " + str(Score2))
print("Third Score: " + str(Score3))
print("Total Score: " + str(TotalScore))
print("Average Score: " + str(AverageScore))

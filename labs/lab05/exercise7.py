import math

# INPUT
Number = float(input("Enter Number: "))

# PROCESS
from math import sqrt, pow, sin, cos
square_root = sqrt(Number)
Square_PowerOf2 = pow(Number,2)
Cube_PowerOf3 = pow(Number,3)
Sine_Value = sin(Number)

# OUTPUTT
print("Square Root : " + str(square_root))
print("Power of 2: " + str(Square_PowerOf2))
print("Power of 3: " + str(Cube_PowerOf3))
print("Sin Value: " + str(Sine_Value))
